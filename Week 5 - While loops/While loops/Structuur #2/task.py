condition = True
counter = 0

#Keyword: while
#Uitgevoerd: Ja
#Conditie variabele(n): condition
#Conditie: condition
#Aantal keer uitgevoerd: 5
while(condition):
    #Keyword(s): if, else
    #Uitgevoerd: Ja
    #Conditie variabele(n): counter
    #Conditie: counter <= 4
    #Aantal keer uitgevoerd: 5
    if(counter <= 4):
        print(counter)
        counter = counter + 1
    else:
        condition = False
